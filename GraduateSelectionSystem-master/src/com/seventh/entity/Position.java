package com.seventh.entity;

public class Position {
	private int id;
	private String name;
	private int level;
	private int maxQuestionNum;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getMaxQuestionNum() {
		return maxQuestionNum;
	}
	public void setMaxQuestionNum(int maxQuestionNum) {
		this.maxQuestionNum = maxQuestionNum;
	} 
	
}
